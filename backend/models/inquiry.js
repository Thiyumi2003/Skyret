import mongoose from "mongoose";

const inquirySchema = new mongoose.Schema({
    firstName: {
        type: String,
        required: true
    },
    lastName: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    phone: {
        type: String
    },
    message: {
        type: String,
        required: true
    },
    date: {
        type: Date,
        default: Date.now
    },
    status: {
        type: String,
        default: "New", // New, Read, Replied
    }
});

const Inquiry = mongoose.model("Inquiry", inquirySchema);
export default Inquiry;